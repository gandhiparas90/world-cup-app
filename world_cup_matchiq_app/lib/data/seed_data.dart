  static Team teamById(String id) {
    return teams.firstWhere((team) => team.id == id);
  }

  static GroupInfo groupById(String id) {
    return groups.firstWhere((group) => group.id == id);
  }

  static TeamStats teamStatsById(String teamId) {
    return teamStats.firstWhere((stats) => stats.teamId == teamId);
  }

  static WorldCupMatch matchById(String id) {
    return matches.firstWhere((match) => match.id == id);
  }

  static List<WorldCupMatch> matchesForTeam(String teamId) {
    return matches
        .where(
          (match) => match.homeTeamId == teamId || match.awayTeamId == teamId,
        )
        .toList();
  }

  static List<WorldCupMatch> upcomingMatches({int limit = 12}) {
    return matches.where((match) => match.isScheduled).take(limit).toList();
  }

  static List<Player> playersForTeam(String teamId) {
    return players.where((player) => player.teamId == teamId).toList();
  }

  static List<Player> playersForMatch(String matchId) {
    final match = matchById(matchId);
    final matchTeamIds = {match.homeTeamId, match.awayTeamId};
    return players
        .where((player) => matchTeamIds.contains(player.teamId))
        .toList();
  }

  static List<WatchOption> watchOptionsForMatch(String matchId) {
    return watchOptions.where((option) => option.matchId == matchId).toList();
  }

  static List<NewsItem> newsForTeam(String teamId) {
    return newsItems.where((item) => item.teamId == teamId).toList();
  }

  static GroupInfo _group(String id, List<String> teamIds) {
    return GroupInfo(id: id, name: 'Group $id', teamIds: teamIds);
  }

